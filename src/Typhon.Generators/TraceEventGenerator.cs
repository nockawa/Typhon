    private sealed class TraceEventModel
    {
        public TraceEventModel(string ns, string structName, string kindName, string codecFqn, OptionalFieldModel[] optionals,
            BeginParamModel[] beginParams, PayloadFieldModel[] payloadFields, string factoryName, bool generateFactory, bool emitEncoder)
        {
            Namespace = ns;
            StructName = structName;
            KindName = kindName;
            CodecFqn = codecFqn;
            Optionals = optionals;
            BeginParams = beginParams;
            PayloadFields = payloadFields;
            FactoryName = factoryName;
            GenerateFactory = generateFactory;
            EmitEncoder = emitEncoder;
        }
        public string Namespace { get; }
        public string StructName { get; }
        public string KindName { get; }
        public string CodecFqn { get; }
        public OptionalFieldModel[] Optionals { get; }
        public BeginParamModel[] BeginParams { get; }
        public PayloadFieldModel[] PayloadFields { get; }
        public string FactoryName { get; }
        public bool GenerateFactory { get; }
        public bool EmitEncoder { get; }
    }

    private sealed class PayloadFieldModel
    {
        public PayloadFieldModel(string fieldName, string typeFqn, string wireTypeFqn, bool isEnum)
        {
            FieldName = fieldName;
            TypeFqn = typeFqn;
            WireTypeFqn = wireTypeFqn;
            IsEnum = isEnum;
        }
        public string FieldName { get; }
        public string TypeFqn { get; }
        public string WireTypeFqn { get; }
        public bool IsEnum { get; }
    }

    private sealed class OptionalFieldModel
    {
        public OptionalFieldModel(string fieldName, string typeFqn, string wireTypeFqn, bool isEnum, string propertyName, string maskConstant)
        {
            FieldName = fieldName;
            TypeFqn = typeFqn;
            WireTypeFqn = wireTypeFqn;
            IsEnum = isEnum;
            PropertyName = propertyName;
            MaskConstant = maskConstant;
        }
        public string FieldName { get; }
        public string TypeFqn { get; }
        /// <summary>For enum fields, the underlying primitive type used for wire encoding (e.g. "byte"). Same as TypeFqn for non-enums.</summary>
        public string WireTypeFqn { get; }
        public bool IsEnum { get; }
        public string PropertyName { get; }
        public string MaskConstant { get; }
    }

    private sealed class BeginParamModel
    {
        public BeginParamModel(string fieldName, string fieldTypeFqn, string paramName, string paramTypeOverride)
        {
            FieldName = fieldName;
            FieldTypeFqn = fieldTypeFqn;
            ParamName = paramName;
            ParamTypeOverride = paramTypeOverride;
        }
        public string FieldName { get; }
        public string FieldTypeFqn { get; }
        public string ParamName { get; }
        /// <summary>When non-null, the factory parameter is typed as this and the body emits an explicit cast.</summary>
        public string ParamTypeOverride { get; }
        public string ParamTypeFqn => ParamTypeOverride ?? FieldTypeFqn;
        public bool NeedsCast => ParamTypeOverride != null;
    }
